function updateLanguage(lang) {
  document.querySelectorAll("[data-t]").forEach((el) => {
    const key = el.getAttribute("data-t");
    const keys = key.split(".");
    let translation = translations[lang];
    keys.forEach((k) => {
      translation = translation[k];
    });
    if (
      el.tagName === "A" &&
      el.hasAttribute("aria-label") &&
      keys[0] === "contact"
    ) {
      // handle special case for contact links if needed, but usually innerHTML is fine
    }
    el.innerHTML = translation;
  });

  // Update lang attribute
  document.documentElement.lang = lang;

  // Update meta tags (optional but good for SEO)
  const metaDesc = document.querySelector('meta[name="description"]');
  if (lang === "en") {
    metaDesc.setAttribute(
      "content",
      "Akka Copywriting helps companies with premium products build trust and drive business through strategic newsletters and editorial content.",
    );
  } else {
    metaDesc.setAttribute(
      "content",
      "Akka Copywriting hjälper företag med premiumprodukter att bygga förtroende och driva affärer genom strategiska nyhetsbrev och redaktionellt innehåll.",
    );
  }

  // Toggle active class on buttons
  document.querySelectorAll(".lang-btn").forEach((btn) => {
    btn.classList.toggle("active", btn.getAttribute("data-lang") === lang);
  });

  // Save preference
  localStorage.setItem("preferredLang", lang);
}

document.querySelectorAll(".lang-btn").forEach((btn) => {
  btn.addEventListener("click", () => {
    const lang = btn.getAttribute("data-lang");
    updateLanguage(lang);
  });
});

// Initial load
const savedLang = localStorage.getItem("preferredLang") || "sv";
updateLanguage(savedLang);
